package paquet;
//structure pour stoke les donnees	dun objet 
public class ObjetGit {
	public String nom;
	public String chemin;
	public String type;
	public String dossier;
	public String donnees;
	
	public ObjetGit(String nom, String chemin, String type, String dossier, String donnees) {
		this.nom = nom;
		this.chemin = chemin;
		this.type = type;
		this.dossier = dossier;
		this.donnees = donnees;
	}
}
